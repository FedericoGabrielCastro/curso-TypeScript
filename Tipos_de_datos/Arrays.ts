// Array acepta todos los valores 

let cursos: string[] // se indica los tipos de valores que tiene el Array 

cursos = [
    'Flutter', 'React.js', 'React.Native'
]

console.log(cursos)

// Se puede declara como tipo de dato "Any" para que el Array pueda recivir cualquier valor 

let cursos2: any[] // se indica los tipos de valores que tiene el Array 

cursos2 = [
    'Flutter', true, -1 , "React.js"
]

console.log(cursos)